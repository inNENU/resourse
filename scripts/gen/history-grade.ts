import type { SelectConfig } from "./typings.js";

class Dsy {
  private config: SelectConfig;
  constructor() {
    this.config = [];
  }

  /**
   *
   * @param {string} id
   * @param {string[]} content
   */
  add(id: string, content: string[]): void {
    const ids = id.split("_").map((item) => Number(item));

    // year
    if (ids.length === 1) {
      content.forEach((item, index) => {
        if (!this.config[index]) this.config[index] = { year: "", items: [] };

        this.config[index].year = item;
      });
    }
    // province
    else if (ids.length === 2) {
      if (!this.config[ids[1]]) this.config[ids[1]] = { year: "", items: [] };

      content.forEach((item, index) => {
        if (!this.config[ids[1]].items[index])
          this.config[ids[1]].items[index] = { province: "", items: [] };

        this.config[ids[1]].items[index].province = item;
      });
    }
    // plan
    else if (ids.length === 3) {
      if (!this.config[ids[1]]) this.config[ids[1]] = { year: "", items: [] };

      if (!this.config[ids[1]].items[ids[2]])
        this.config[ids[1]].items[ids[2]] = { province: "", items: [] };

      content.forEach((item, index) => {
        if (!this.config[ids[1]].items[ids[2]].items[index])
          this.config[ids[1]].items[ids[2]].items[index] = {
            plan: "",
            items: [],
          };

        this.config[ids[1]].items[ids[2]].items[index].plan = item;
      });
    }
    // category
    else if (ids.length === 4) {
      if (!this.config[ids[1]]) this.config[ids[1]] = { year: "", items: [] };

      if (!this.config[ids[1]].items[ids[2]])
        this.config[ids[1]].items[ids[2]] = { province: "", items: [] };

      if (!this.config[ids[1]].items[ids[2]].items[ids[3]])
        this.config[ids[1]].items[ids[2]].items[ids[3]] = {
          plan: "",
          items: [],
        };

      content.forEach((item, index) => {
        if (!this.config[ids[1]].items[ids[2]].items[ids[3]].items[index])
          this.config[ids[1]].items[ids[2]].items[ids[3]].items[index] = {
            category: "",
            items: [],
          };

        this.config[ids[1]].items[ids[2]].items[ids[3]].items[index].category =
          item;
      });
    }
    // class
    else if (ids.length === 5) {
      if (!this.config[ids[1]]) this.config[ids[1]] = { year: "", items: [] };

      if (!this.config[ids[1]].items[ids[2]])
        this.config[ids[1]].items[ids[2]] = { province: "", items: [] };

      if (!this.config[ids[1]].items[ids[2]].items[ids[3]])
        this.config[ids[1]].items[ids[2]].items[ids[3]] = {
          plan: "",
          items: [],
        };

      if (!this.config[ids[1]].items[ids[2]].items[ids[3]].items[ids[4]])
        this.config[ids[1]].items[ids[2]].items[ids[3]].items[ids[4]] = {
          category: "",
          items: [],
        };

      content.forEach((item, index) => {
        if (
          !this.config[ids[1]].items[ids[2]].items[ids[3]].items[ids[4]].items[
            index
          ]
        )
          this.config[ids[1]].items[ids[2]].items[ids[3]].items[ids[4]].items[
            index
          ] = { type: "" };

        this.config[ids[1]].items[ids[2]].items[ids[3]].items[ids[4]].items[
          index
        ].type = item;
      });
    }
  }

  exportJSON(): string {
    return JSON.stringify(this.config);
  }
}

